class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        int curr = 0;
        int maxTillNow = 0;
        // Your code here
        for(int i = 0; i < n; i++){
            curr += arr[i];
            maxTillNow = max(curr, maxTillNow);

            if(curr < 0){
                continue;
            }
        }

        return maxTillNow;
    }
};

